# Print addition of #s
puts 1+2

# Ruby has integer/float interactions much like java, etc
puts 10/3

# Multiplying string by number n displays n instances of that number
print "-" * 3

# Can execute methods n times as well
# rand(n) generates a # between 0 and n-1 
4.times {puts rand(10)}

# Objects that cannot be converted to a number are set to 0
puts "hello".to_i


